<section id="rirekikanri" class="anchor">
  <h2 class="ttl ">住宅履歴管理</h2>
  <h3 class="ttl my-4">住宅履歴情報を管理・蓄積し、維持管理に役立てています。</h3>
  <p> 当社では、住宅がどのようなつくりで、どのような性能があるか、<br>
    また建築後に行われた点検・修繕・リフォーム等の記録を蓄積しています。 </p>
  <figure class="w100  mx-auto"> <img src="<?php bloginfo('stylesheet_directory'); ?>/images/builder/rirekikanri001.webp" alt="住宅履歴管理"> </figure>
  <p class="mb-4 mb-sm-5">住宅履歴情報を蓄積することで、建物について詳細に把握し、維持管理に役立てています。</p>
  <h4 class="ttl brackets2 bold txt-0">点検</h4>
  <ul class="pl-2 pl-sm-4">
    <li> あらかじめ住宅の建て方や仕様（使用されている製品等）、過去の点検結果やリフォームの内容を記録しています。 </li>
  </ul>
  <h4 class="ttl brackets2 bold txt-0">不具合の発生 </h4>
  <ul class="pl-2 pl-sm-4">
    <li>不具合が発生した際に、使用されている製品や建築時の情報により、早期に原因を特定し、修繕することができます。</li>
  </ul>
  <h4 class="ttl brackets2 bold txt-0">リフォーム</h4>
  <ul class="pl-2 pl-sm-4">
    <li>図面や過去の修繕記録から正確に現状を把握することができ、必要なリフォーム工事の内容を効率的かつ的確に計画することができます。</li>
  </ul>
  <p> 「住宅履歴情報が管理された住宅」だから「快適な環境で永く住める住宅」を実現できます。 </p>
</section>
