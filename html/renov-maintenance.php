<section id="tenkenkou" class="anchor">
  <h2 class="ttl ">点検口</h2>
  <h3 class="ttl my-4">点検口とは</h3>
  <p class="mb-5">点検口とは、天井や床下などの配線や配管を見るための入口のことです。<br>
    建物の維持管理において重要であり、点検口があると経年劣化による漏水事故などの防止や、建物の傷み具合を定期的に点検することができます。<br>
    点検口があると、問題が発生したときに迅速に対応ができ、修繕費用を安く抑えることができます。天井や床下をくまなく点検できるよう、必要な箇所にきちんと点検口が設置されている建物は、長期メンテナンスに配慮した建物だといえます。<br>
    点検口は、主に天井点検口と床下点検口があります。 </p>
  <div class="row">
    <div class="col-sm-7">
      <h4 class="ttl brackets2 bold my-3">天井点検口</h4>
      <p class="pl-3 pl-sm-4 border-left">天井点検口は、主に電気系統の配線が通っています。<br>
        天井点検口があれば、換気ダクトの取り付け状態、電気配線の整理状況や断熱材の有無と施工状態などか定期的に確認できます。<br>
        また、将来のライフスタイルの変化に伴う間取りの変更で、配線などがフレキシブルに対応できます。 </p>
    </div>
    <figure class="w100 col-sm-5 px-4 pl-md-5 align-self-end"> <img src="<?php bloginfo('stylesheet_directory'); ?>/images/builder/tenkenkou003.webp" alt="天井点検口"> </figure>
  </div>
  <div class="row">
    <div class="col-sm-7">
      <h4 class="ttl brackets2 bold my-3">床下点検口</h4>
      <p class="pl-3 pl-sm-4 border-left">床下点検口は、主に排水系のパイプが通っています。<br>
        床下点検口は、床下収納庫を兼ねていることが非常に多いです。床下のスペースを有効活用できる床下収納庫ですが、収納の蓋を開けて中のボックスを取り出せば、点検口になります。<br>
        給排水管などが劣化した場合や、白アリ被害が出た場合も床下点検口があれば、すばやく被害状況をチェックし、修繕、対応することが可能になります。 </p>
    </div>
    <figure class="w100 col-sm-5 px-4 pl-md-5 align-self-end"> <img src="<?php bloginfo('stylesheet_directory'); ?>/images/builder/tenkenkou001.webp" alt="床下点検口"> </figure>
  </div>
</section>
